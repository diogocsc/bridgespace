def get_translations(lang: str) -> Dict[str, str]:
    """Return the full dict of UI strings for the given language code. Falls back to English."""
    return LOCALES.get(lang, LOCALES["en"]).copy()


def translate(key: str, lang: str) -> str:
    """Return a single translated string. Falls back to English then to the key itself."""
    d = LOCALES.get(lang, LOCALES["en"])
    return d.get(key, LOCALES["en"].get(key, key))
